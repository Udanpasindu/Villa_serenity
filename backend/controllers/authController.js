const User = require('../models/User');
const sendEmail = require('../utils/sendEmail');
const crypto = require('crypto');

// @desc    Register user
// @route   POST /api/auth/register
// @access  Public
exports.register = async (req, res) => {
  console.log('Registration request received:', {
    name: req.body.name,
    email: req.body.email,
    password: req.body.password ? '[MASKED]' : 'undefined'
  });
  
  try {
    const { name, email, password } = req.body;
    
    // Basic validation
    if (!name || !email || !password) {
      console.log('Missing required fields');
      return res.status(400).json({
        success: false,
        error: 'Please provide name, email and password'
      });
    }

    // Check if user already exists
    const existingUser = await User.findOne({ email });
    if (existingUser) {
      console.log('User already exists with email:', email);
      return res.status(400).json({
        success: false,
        error: 'Email already registered'
      });
    }

    console.log('Creating new user:', name, email);
    
    // Create user - explicitly define fields to avoid unwanted data
    const user = new User({
      name,
      email,
      password,
      role: 'user' // Default role
    });
    
    // Save user - separated from creation for better error handling
    await user.save();
    
    console.log('User created successfully:', user._id);
    
    // Generate token
    const token = user.getSignedJwtToken();
    
    // Return response directly instead of using helper function
    res.status(201).json({
      success: true,
      token,
      user: {
        id: user._id,
        name: user.name,
        email: user.email,
        role: user.role
      }
    });
  } catch (err) {
    console.error('Registration error:', err);
    
    // Handle specific MongoDB errors
    if (err.code === 11000) {
      return res.status(400).json({
        success: false,
        error: 'Email already exists'
      });
    }
    
    // Handle validation errors
    if (err.name === 'ValidationError') {
      const messages = Object.values(err.errors).map(val => val.message);
      return res.status(400).json({
        success: false,
        error: messages.join(', ')
      });
    }
    
    res.status(500).json({
      success: false,
      error: 'Server error'
    });
  }
};

